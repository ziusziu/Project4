public class Fare {
    String kind;
    String id;
    String carrier;
    String origin;
    String destination;
    String basisCode;

    public String getKind() {
        return kind;
    }

    public String getId() {
        return id;
    }

    public String getCarrier() {
        return carrier;
    }

    public String getOrigin() {
        return origin;
    }

    public String getDestination() {
        return destination;
    }

    public String getBasisCode() {
        return basisCode;
    }
}
