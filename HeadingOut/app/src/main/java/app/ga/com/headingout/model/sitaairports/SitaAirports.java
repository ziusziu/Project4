public class SitaAirports {
    String code;
    String name;
    String city;
    String country;
    String timezone;
    double lat;
    double lng;
    String terminal;
    String gate;

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public String getCity() {
        return city;
    }

    public String getCountry() {
        return country;
    }

    public String getTimezone() {
        return timezone;
    }

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }

    public String getTerminal() {
        return terminal;
    }

    public String getGate() {
        return gate;
    }
}
